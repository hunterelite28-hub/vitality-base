/**
 * Preset training rotations the user can pick from in the setup wizard.
 *
 * Each preset defines a default day list. Once picked, the user can rename
 * days, mark heavy/volume/rest, add or remove days. The customized result
 * is persisted to training_settings.rotation_days.
 *
 * Curation reflects the 2026-05-25 research synthesis: 4 evidence-backed
 * v1 splits drawn from Schoenfeld 2016 meta-analysis (twice-weekly
 * frequency), Helms Pyramid (adherence-first), and real-world adherence
 * data (Conti et al. 522k-user SportRxiv 709). v1.1 will add PHUL,
 * 5/3/1 BBB, and Bro Split; v1.2 will gate PHAT + Arnold behind an
 * Advanced toggle.
 */

import type { DayType, Category, DayExercise } from '../log/splitData'
import type { GoalPreferences } from '@/lib/preferences'

export interface PresetDay {
  name: string
  type: DayType
  category: Category
  exercises: DayExercise[]
  /** Active-rest metadata. Present only on rest days that the
   *  recommendation engine has upgraded based on the user's intake quiz
   *  (cardio appetite, sport, recovery). The day is still a recovery
   *  day from a fatigue standpoint — the activity is what they do on it.
   *  Persisted via the same rotation_days JSON path as the rest of the
   *  day, but always regenerable from intake so a missing field is OK. */
  activeRest?: ActiveRest
}

/**
 * The activity slotted into an upgraded rest day. Generated by the
 * recommendation engine from intake answers; users can swap or revert
 * to full rest via the day-row change chip in step 2.
 */
export interface ActiveRest {
  activity: ActiveRestActivity
  /** Duration in minutes. The engine picks a sensible default per
   *  activity, but users can override in the activity-picker sheet
   *  (we can't guess every individual's session length). */
  durationMin: number
  /** Short engine-generated copy explaining why this slot got this
   *  activity — surfaces in the row + change-activity sheet so the
   *  user understands the matching, not just the suggestion. */
  notes?: string
  /** User-typed label, only set when activity === 'custom'.
   *  Lets users define anything outside our preset list
   *  (e.g. "Breathwork", "Pickleball", "Sauna"). */
  customLabel?: string
}

export type ActiveRestActivity =
  | 'cardio_zone2'      // steady-state aerobic — base building
  | 'cardio_intervals'  // intervals — for cardio "primary" users
  | 'sport'             // user's existing sport, kept as-is
  | 'yoga'              // structured yoga practice — distinct from generic mobility
  | 'mobility'          // mobility + soft tissue work — stressed-recovery default
  | 'walk'              // lightest option — for rough-recovery weeks
  | 'custom'            // user-defined — uses customLabel for the display name

/**
 * Beginner-friendliness rating — drives the green/yellow/red dot on the
 * split picker card. "beginner" = recommended for first-timers, simplest
 * schedule. "advanced" = high volume, demanding programming.
 */
export type SplitLevel = 'beginner' | 'intermediate' | 'advanced'

export interface Preset {
  id:
    | 'full_body_2'  // minimalist 2-day — busy / returning lifter
    | 'full_body_3'  // classic 3-day novice template (Stronglifts-style)
    | 'upper_lower_4' // research-validated intermediate default
    | 'ppl_3'         // standard Mon/Wed/Fri PPL — what most casuals actually run
    | 'bro_5'         // chest / back / shoulders / arms / legs — aesthetic-focused intermediate
    | 'ppl_6'         // advanced hypertrophy specialist (PPL ×2)
    | 'blank'
  name: string
  meta: string
  /** One-line plain-English description of what the split is for. */
  lede?: string
  /** Beginner-friendliness rating (driven by gym-floor reality, not
   *  programming geek-out). Affects the colored dot on the card. */
  level?: SplitLevel
  /** When true, the days array is a rotating cycle that drifts through
   *  the calendar week (e.g. classic PPL — push/pull/legs/rest repeat).
   *  Drives the card's day-strip rendering: session-letter labels
   *  (P · P · L · R · ...) instead of weekday letters, plus a ↻ glyph
   *  in the meta line. Default false = fixed weekly schedule. */
  rotating?: boolean
  /** Plain-English explanation shown in the per-card info popover when
   *  the user taps the (i). 2–3 sentences max — who it's for, what it
   *  hits, why someone would pick it. Skip jargon. */
  info?: string
  /** 3 short scannable highlights rendered as mint-checkmark bullets at
   *  the top of the info popover, ABOVE the full paragraph. Each one
   *  ≤8 words. Lets a user judge "is this for me?" without reading prose. */
  infoHighlights?: string[]
  days: PresetDay[]
}

