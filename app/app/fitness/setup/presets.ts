/**
 * Recommend a split + diagnostic from the 11-question intake.
 *
 * Pipeline:
 *  1. Compute effective lifting days (start from `days`, subtract for
 *     outside-training fatigue + rough recovery).
 *  2. Pick a base split from (effective days × experience).
 *  3. Goal nudges the split at edges (strength keeps it on U/L over PPL).
 *  4. Goal-preferences correction layer — when the user has set their
 *     Vitality Goal (`outcome` + `window` + `constraint`), apply
 *     strategic overrides on top of the tactical intake decisions. Goal
 *     is the north star; intake is the logistics. See applyGoalCorrection.
 *  5. Build diagnostic + reasoning from the full answer set (including
 *     any Goal-driven overrides so the user sees why we steered them).
 */
export function recommendIntake(
  a: IntakeAnswers,
  goal?: GoalPreferences | null,
): IntakeRecommendation {
  // ── Effective lifting days ────────────────────────────────────────
  // Outside-training fatigue + sub-par recovery both subtract budget.
  let effective: Days = a.days
  let override: IntakeRecommendation['daysOverride'] | undefined

  if (a.recovery === 'rough' && a.days >= 5) {
    effective = 4
    override = {
      requested: a.days,
      recommended: 4,
      why: 'recovery is rough right now, and five-plus days a week reliably under-recovers and stalls everything. Four days a week protects your gains.',
    }
  } else if (a.cardioAndOutside === 'sport' && a.days >= 5) {
    effective = 4
    override = {
      requested: a.days,
      recommended: 4,
      why: 'a sport that demands recovery is already eating into your weekly fatigue budget. Four lifting days leaves room for it.',
    }
  } else if (a.cardioAndOutside === 'running' && a.days >= 5) {
    effective = 4
    override = {
      requested: a.days,
      recommended: 4,
      why: 'regular runs already tax your legs and recovery. Four lifting days leaves the running room to compound.',
    }
  } else if (a.cardioAndOutside === 'cardio_first' && a.days >= 5) {
    effective = 3
    override = {
      requested: a.days,
      recommended: 3,
      why: 'cardio is your primary thing, and three lifting days complement it without competing for recovery.',
    }
  }

  // Note: `a.recovery === 'rough' && a.days <= 2` deliberately gets no
  // override. Two days a week is already conservative — overriding it
  // downward would mean recommending 1 day or 0, which isn't a program.
  // The reasoning bullet still flags the fatigue picture.

  // ── Base split from (effective days × experience) ───────────────
  const beginnerOrReturning = a.experience === 'new' || a.experience === 'back_long'
  let presetId: Preset['id']

  // Experienced specialists earn the advanced H/V variant of PPL; the
  // "some experience" intermediate gets the classic PPL rotation instead.
  const advancedPPLCandidate = a.experience === 'experienced'

  // ── Style override — user preference dominates research-optimal default ──
  // The BUILD20 "how do you actually like to train?" question gives the
  // user agency: when they explicitly picked a style, we honor it within
  // day-count compatibility. Adherence to a split you like beats the
  // optimal split you skip. Beginners still get full_body regardless
  // (technique acquisition takes priority over preference at zero
  // experience). `surprise_me` falls through to the research defaults.
  const honorStyle = !!a.style && a.style !== 'surprise_me' && !beginnerOrReturning

  if (honorStyle && a.style === 'one_body_part' && effective >= 4) {
    presetId = 'bro_5'   // 4 or 6: also bro_5; user can trim/extend days in step 2
  } else if (honorStyle && a.style === 'ppl' && effective >= 4) {
    // Classic PPL is fundamentally a 6-lifting-day rotation (push, pull,
    // legs, rest, repeat). At 4–5 days we still ship it — the trailing
    // days get adjusted in step 2. At 6+, the advanced specialist gets
    // the H/V version; intermediates get the classic rotation.
    presetId = advancedPPLCandidate && effective >= 6 ? 'ppl_6' : 'ppl_3'
  } else if (honorStyle && a.style === 'ppl' && effective === 3) {
    // True 3-day PPL doesn't exist — once-per-week per muscle is below
    // the meta-analysis frequency floor. Route to full_body_3 (hits
    // each muscle 3× a week) and the reasoning bullet explains why.
    presetId = 'full_body_3'
  } else if (honorStyle && a.style === 'upper_lower' && effective >= 4) {
    presetId = 'upper_lower_4'
  } else if (honorStyle && a.style === 'full_body') {
    presetId = effective <= 2 ? 'full_body_2' : 'full_body_3'
  } else if (effective <= 2) {
    presetId = 'full_body_2'
  } else if (effective === 3) {
    // True 3-day PPL doesn't exist as a real frequency split. At 3 days
    // a week, full body 3× hits each muscle 3×/week — strictly better
    // than any once-per-week PPL Mon/Wed/Fri arrangement.
    presetId = 'full_body_3'
  } else if (effective === 4) {
    presetId = beginnerOrReturning ? 'full_body_3' : 'upper_lower_4'
  } else if (effective === 5) {
    // 5 days = bro split's exact slot. Classic PPL needs 6, U/L needs 4 —
    // five is the bro split's natural fit. Strength still wins on U/L
    // because heavy compounds need the recovery days.
    if (beginnerOrReturning) {
      presetId = 'full_body_3'
    } else if (a.goal === 'strength') {
      presetId = 'upper_lower_4'
    } else {
      presetId = 'bro_5'
    }
  } else {
    // 6 effective days
    if (beginnerOrReturning) {
      presetId = 'full_body_3'  // never PPL for a beginner, no matter what
    } else if (a.goal === 'strength') {
      presetId = 'upper_lower_4' // heavy compounds need recovery — U/L wins
    } else if (advancedPPLCandidate) {
      presetId = 'ppl_6'   // experienced specialist gets the H/V variant
    } else {
      presetId = 'ppl_3'   // intermediate gets the classic PPL rotation
    }
  }

  // ── Session length veto ──────────────────────────────────────────
  // Under 45 min can't sustain the high-volume specialized splits —
  // downgrade to U/L (lower per-session volume, twice-a-week frequency).
  // Classic PPL (ppl_3, 6 sessions on rotation, no H/V split) still demands meaningful
  // per-session volume so it's vetoed too.
  if (a.session === 'under_45' && (presetId === 'ppl_6' || presetId === 'bro_5' || presetId === 'ppl_3')) {
    presetId = 'upper_lower_4'
  }

  // ── Restriction veto ─────────────────────────────────────────────
  // A user flagging `lower_back` can't sustain six lifting days of
  // posterior-chain work (ppl_6's pull days lead with heavy deadlift).
  // Even with the substitution pass in exerciseSelection.ts, the
  // frequency is rough on a restricted lower back. Downgrade to U/L
  // (one heavy lower day per week, easy to swap squats/DL for leg
  // press + lying-leg-curl). bro_5's leg day is similarly avoidable.
  if (a.restrictions.includes('lower_back') && (presetId === 'ppl_6' || presetId === 'bro_5')) {
    presetId = 'upper_lower_4'
  }

  // ── Goal correction layer ────────────────────────────────────────
  // The user's Vitality Goal is the north star; the intake handles the
  // tactical "how does this fit my life" details. When Goal disagrees
  // with the intake's choice on intent (cut vs bulk, longevity vs peak
  // volume), Goal wins and we collect a reasoning bullet so the user
  // sees why we steered. When they disagree on capacity (days, gear),
  // intake wins.
  const goalBullets: string[] = []
  if (goal) {
    const corrected = applyGoalCorrection(presetId, effective, a, goal)
    presetId = corrected.presetId
    goalBullets.push(...corrected.bullets)
    if (corrected.daysOverride) {
      // Goal-driven days override stacks on top of any earlier one. If
      // both exist, the later (Goal) note is more strategic — keep it.
      override = corrected.daysOverride
    }
  }

  const diagnosticHeadline = splitHeadline(presetId, a)
  const diagnosticReason   = splitReason(presetId, a)
  const diagnosticPersonal = personalClose(a, override, goal)
  // Combined string kept for back-compat with any consumer that doesn't
  // know about the structured parts. The hero UI now renders the parts
  // individually so each gets distinct visual weight.
  const diagnostic = `${diagnosticHeadline} ${diagnosticReason} ${diagnosticPersonal}`
  const reasoning  = buildReasoning(a, presetId, override, goalBullets)
  const factors    = buildFactors(a, presetId, override)

  return {
    presetId,
    diagnostic,
    diagnosticHeadline,
    diagnosticReason,
    diagnosticPersonal,
    reasoning,
    factors,
    daysOverride: override,
  }
}

/**
 * Goal-preferences correction layer. Runs after the intake decision tree
 * has picked a `presetId` and applied its own vetoes. Returns the
 * possibly-corrected split, plus any reasoning bullets explaining the
 * Goal-driven overrides (which `buildReasoning` weaves into the diagnostic
 * card so the user knows Goal is what changed things).
 *
 * Design intent — see brainstorm 2026-05-28: Goal owns *intent*
 * (cut/bulk/longevity), intake owns *capacity* (days, equipment,
 * restrictions). Goal corrections cap or shift the intake choice but
 * never override hard intake-stated capacity.
 */
function applyGoalCorrection(
  presetId: Preset['id'],
  effective: Days,
  a: IntakeAnswers,
  goal: GoalPreferences,
): {
  presetId: Preset['id']
  bullets: string[]
  daysOverride?: IntakeRecommendation['daysOverride']
} {
  const bullets: string[] = []
  let next = presetId
  let daysOverride: IntakeRecommendation['daysOverride'] | undefined

  const beginnerOrReturning = a.experience === 'new' || a.experience === 'back_long'

  // Rule 1 — cut + short window → cap to a recoverable 3-4 day split.
  // A calorie deficit eats recovery; high-frequency / high-volume splits
  // stall fast or actively cost muscle. We cap aggressively and tell the
  // user why.
  if (goal.outcome === 'cut' && (goal.window === '30' || goal.window === '60')) {
    if (next === 'ppl_6' || next === 'bro_5' || next === 'ppl_3') {
      const requestedDays = a.days
      const cappedDays: Days = effective >= 4 ? 4 : 3
      next = cappedDays === 4 ? 'upper_lower_4' : 'full_body_3'
      bullets.push(`You're cutting on a ${goal.window}-day window, and a deficit eats recovery. We capped this at ${cappedDays} days so you keep the muscle you're trying to lean out.`)
      if (cappedDays < requestedDays) {
        daysOverride = {
          requested: requestedDays,
          recommended: cappedDays,
          why: `you're cutting on a ${goal.window}-day window. Eating in a deficit means recovery is the scarce resource. ${cappedDays} hard lifting days protects the muscle you're trying to lean out far better than ${requestedDays} half-recovered ones.`,
        }
      }
    }
  }

  // Rule 2 — longevity outcome → joint-friendly, sustainable. Cap to
  // full body 2-3 day unless the user *explicitly* picked more days
  // through `style` (then we respect the agency).
  if (goal.outcome === 'longevity' && a.style !== 'one_body_part' && a.style !== 'ppl') {
    if (next === 'ppl_6' || next === 'bro_5' || next === 'ppl_3' || next === 'upper_lower_4') {
      const cappedDays: Days = effective >= 3 ? 3 : 2
      next = cappedDays === 3 ? 'full_body_3' : 'full_body_2'
      bullets.push(`Longevity-first goal means joint preservation and consistency over peak volume. We put you on a sustainable ${cappedDays}-day full-body rhythm you can run for decades.`)
      if (cappedDays < a.days) {
        daysOverride = {
          requested: a.days,
          recommended: cappedDays,
          why: `longevity-first training is about consistency over decades. ${cappedDays} sustainable days a week protect your joints and stick rate far better than ${a.days} aggressive ones.`,
        }
      }
    }
  }

  // Rule 3 — bulk + long window + no time pressure + intermediate+ → push
  // toward higher-frequency splits when days allow. Hypertrophy needs
  // frequency × volume; a long window earns the right to ramp.
  if (
    goal.outcome === 'bulk'
    && (goal.window === '90' || goal.window === '180' || goal.window === 'open')
    && goal.constraint !== 'time'
    && !beginnerOrReturning
    && effective >= 5
    && (next === 'upper_lower_4' || next === 'full_body_3')
  ) {
    next = a.experience === 'experienced' && effective >= 6 ? 'ppl_6' : 'ppl_3'
    bullets.push(`Building muscle on a ${goal.window === 'open' ? 'no-deadline' : `${goal.window}-day`} window means earning higher frequency. We moved you up to a PPL rotation so each muscle gets the volume it needs to grow.`)
  }

  // Rule 4 — constraint=injury → veto the two highest-volume / risk
  // splits regardless of what intake said.
  if (goal.constraint === 'injury' && (next === 'ppl_6' || next === 'bro_5')) {
    next = 'upper_lower_4'
    bullets.push('You flagged injury as the thing most likely to get in the way. We dropped you off the highest-volume splits so you have room to manage flare-ups.')
  }

  // Rule 5 — constraint=time → hard cap at a 4-day split. A user who said
  // "time is the constraint" can't sustain bro_5/ppl_6/ppl_3 long-term.
  if (goal.constraint === 'time' && (next === 'ppl_6' || next === 'bro_5' || next === 'ppl_3')) {
    next = 'upper_lower_4'
    bullets.push("Time is your constraint, so we kept this at four days max. Adherence to a split that fits beats the optimal split you skip.")
  }

  return { presetId: next, bullets, daysOverride }
}

// ────────────────────────────────────────────────────────────────────────────
// Diagnostic + reasoning generators
// ────────────────────────────────────────────────────────────────────────────
// Template-based for v1. The shape is intentionally simple so a future
// Mentor LLM call (passing the IntakeAnswers + chosen presetId) can drop
// in here without changing the call site.

function buildDiagnostic(
  a: IntakeAnswers,
  presetId: Preset['id'],
  override: IntakeRecommendation['daysOverride'],
  goal?: GoalPreferences | null,
): string {
  // Combined string form, kept here for any consumer that still wants
  // the concatenated diagnostic. The hero UI uses the three parts
  // (splitHeadline / splitReason / personalClose) directly.
  return `${splitHeadline(presetId, a)} ${splitReason(presetId, a)} ${personalClose(a, override, goal)}`
}

/** The split name as a confident one-line headline. No em dashes.
 *  This is what renders as the large mint-serif headline on the hero. */
function splitHeadline(presetId: Preset['id'], _a: IntakeAnswers): string {
  switch (presetId) {
    case 'full_body_2':   return `Two days, Full Body.`
    case 'full_body_3':   return `Three days, Full Body.`
    case 'upper_lower_4': return `Four days, Upper / Lower.`
    case 'ppl_3':         return `Push / Pull / Legs on rotation.`
    case 'bro_5':         return `Five days, one body part each.`
    case 'ppl_6':         return `Six days, PPL with heavy and volume rotation.`
    case 'blank':
    default:              return `Your custom split.`
  }
}

/** The research backing for the split, plain prose under the headline.
 *  Cites Schoenfeld landmarks where they earn their keep so the user
 *  reads the recommendation as evidence-backed. */
function splitReason(presetId: Preset['id'], a: IntakeAnswers): string {
  switch (presetId) {
    case 'full_body_2':
      return `The highest stick-rate program. Around 80% of full-program gains for 40% of the time.`
    case 'full_body_3':
      if (a.experience === 'new' || a.experience === 'back_long') {
        return `Every lift, three times a week. Fastest technique build at your level.`
      }
      return `Every muscle hit 3 times a week. Top of Schoenfeld's frequency curve.`
    case 'upper_lower_4':
      return `Every muscle hit twice a week. Schoenfeld 2016's sweet spot for hypertrophy.`
    case 'ppl_3':
      return `Twice-weekly per muscle on the calendar. No heavy or volume split.`
    case 'bro_5':
      return `Highest per-session volume per muscle. Focused, adherent, satisfying.`
    case 'ppl_6':
      return `Each muscle gets a heavy day and a volume day. The advanced specialist build.`
    case 'blank':
    default:
      return `Built around your own structure.`
  }
}

/** The personal tie. First matching rule wins so each user reads a
 *  sentence the engine actually authored for them. No em dashes. */
function personalClose(
  a: IntakeAnswers,
  override: IntakeRecommendation['daysOverride'],
  goal?: GoalPreferences | null,
): string {
  if (override) {
    return `You asked for ${override.requested}. We recommend ${override.recommended} so ${override.why}`
  }
  if (a.recovery === 'rough') {
    return `Recovery is rough, so volume is pulled back. Stay in the program. Don't dig a hole.`
  }
  if (a.recovery === 'stressed') {
    return `Recovery is running thin. Weekly volume capped under the textbook so stress doesn't compound.`
  }
  if (a.recovery === 'great' && (a.goal === 'muscle' || a.goal === 'recomp' || a.goal === 'strength')) {
    return `Sleep is solid. Volume climbs week over week as you adapt.`
  }
  if (a.cardioAndOutside === 'cardio_first' && a.goal === 'strength') {
    return `Cardio is your priority, so lifting days stay low. Heavy compounds keep fresh legs.`
  }
  // Goal — only when intake goal is meaningful and the goal-prefs
  // outcome didn't already speak (goalPrefs takes priority if set).
  if (goal) {
    return `Goal: ${goalOutcomeDescriptor(goal.outcome)}.`
  }
  switch (a.goal) {
    case 'strength':
      return `Heavy compounds lead. You build strength first.`
    case 'fat_loss':
      return `Volume stays high to protect muscle through the deficit.`
    case 'health':
      return `Light, recoverable, built to outlast burnout.`
    case 'recomp':
      return `Moderate volume and heavy compounds. Enough to grow, light enough to recover at maintenance.`
    case 'muscle':
      return `Compounds first, isolation in the back half. Hypertrophy rep ranges throughout.`
  }
}

function goalOutcomeDescriptor(o: GoalPreferences['outcome']): string {
  switch (o) {
    case 'cut':       return 'to lose fat while keeping muscle'
    case 'recomp':    return 'to lose fat and add muscle at once'
    case 'bulk':      return 'to build muscle and scale up'
    case 'maintain':  return 'to hold where you are and train consistently'
    case 'longevity': return 'to train for the long arc'
  }
}

function buildReasoning(
  a: IntakeAnswers,
  presetId: Preset['id'],
  override: IntakeRecommendation['daysOverride'],
  goalBullets: string[],
): string[] {
  const bullets: string[] = []

  // Days reasoning
  switch (presetId) {
    case 'full_body_2':
      bullets.push('Two sessions is the highest-stick-rate program, around 80% of full-program gains for 40% of the time')
      break
    case 'full_body_3':
      if (a.experience === 'new' || a.experience === 'back_long') {
        bullets.push('Three full-body sessions builds technique fastest at your experience level, every lift, three times a week')
      } else {
        bullets.push('Three full-body sessions hit each muscle 3× a week, top of Schoenfeld\'s frequency curve')
      }
      break
    case 'upper_lower_4':
      bullets.push('Four upper/lower sessions hit every muscle twice a week, the meta-analysis sweet spot for hypertrophy')
      break
    case 'ppl_3':
      bullets.push('Classic PPL: push, pull, legs, rest, then repeat. Three workouts on rotation, twice-weekly per muscle, no heavy/volume split')
      break
    case 'bro_5':
      bullets.push('Bro Split: one body part a day, highest per-session volume per muscle. Frequency once-a-week, but adherence + focus pay it back')
      break
    case 'ppl_6':
      bullets.push('PPL with heavy + volume rotation: each muscle gets a heavy day and a volume day. The advanced specialist version for years-experienced lifters chasing size')
      break
  }

  // Goal reasoning
  if (a.goal === 'muscle') {
    bullets.push('Muscle-first goal puts isolation work in the back half of every session, pumps after the compound work')
  } else if (a.goal === 'strength') {
    bullets.push('Strength goal means heavy compounds lead each day at 4 to 6 reps, accessories trail')
  } else if (a.goal === 'fat_loss') {
    bullets.push('Fat loss goal keeps volume high to protect muscle while you eat in a deficit')
  } else if (a.goal === 'recomp') {
    bullets.push('Recomp means moderate volume + heavy compounds: enough stimulus to grow, not so much you can\'t recover at maintenance calories')
  } else if (a.goal === 'health') {
    bullets.push('Health-first goal keeps the program light and recoverable so you don\'t burn out')
  }

  // Priority-driven reasoning — note when user picked specific body parts
  if (a.priorities && a.priorities.length > 0 && !a.priorities.includes('balanced')) {
    const names = a.priorities.map(prioritySingular).join(' + ')
    bullets.push(`You want to bias toward ${names}, so we add an extra accessory slot for those on every day they're hit`)
  }

  // Style-driven reasoning — only when user took an explicit preference
  if (a.style && a.style !== 'surprise_me') {
    bullets.push(stylePreferenceBullet(a.style))
  }

  // Restriction adjustments
  for (const r of a.restrictions) {
    bullets.push(restrictionBullet(r))
  }

  // Equipment-driven swap notes
  if (a.equipment === 'dumbbells') {
    bullets.push('Dumbbells only: we swap barbell compounds for DB equivalents (DB bench, DB row, RDL)')
  } else if (a.equipment === 'bodyweight') {
    bullets.push('Bodyweight only: your program leads with pull-ups, dips, push-ups, single-leg squat progressions')
  } else if (a.equipment === 'home_full') {
    bullets.push('Home gym with barbell + rack: full programming, no machine accessories')
  }

  // Recovery reasoning
  if (a.recovery === 'great') {
    bullets.push('Solid sleep + low stress means we can add volume week over week')
  } else if (a.recovery === 'rough' && override) {
    bullets.push(`Recovery override: ${override.why}`)
  } else if (a.recovery === 'stressed') {
    bullets.push('Stressed recovery means we cap weekly volume below the textbook prescription')
  }

  // Cardio + outside-training interaction
  if (a.cardioAndOutside === 'cardio_first' && a.goal === 'strength') {
    // Genuine conflict: cardio-first fatigues CNS + legs, strength demands
    // fresh heavy compounds. The engine already capped lifting days lower
    // (via the cardioAndOutside override path) — call it out so the user
    // knows we saw the trade-off.
    bullets.push('Cardio-first + strength goal is a trade-off. Heavy compounds want fresh legs, so we kept lifting days low so you can run them hard without the cardio stealing recovery')
  } else if (a.cardioAndOutside === 'cardio_first') {
    bullets.push('Cardio is primary, so lifting is structured to support it, not compete for recovery')
  } else if (a.cardioAndOutside === 'running' && (presetId === 'ppl_6' || presetId === 'bro_5' || presetId === 'upper_lower_4' || presetId === 'ppl_3')) {
    bullets.push('Regular runs share recovery with leg work, so we trimmed leg-day volume to keep both honest')
  } else if (a.cardioAndOutside === 'sport' && (presetId === 'ppl_6' || presetId === 'bro_5')) {
    bullets.push('A sport already eats into the weekly fatigue budget, so we kept lifting volume manageable so you can show up for both')
  }

  // Goal-correction bullets last so they read as the "and on top of that,
  // here's how your north star changed the call" punchline. Empty when
  // Goal wasn't set or none of its rules fired.
  bullets.push(...goalBullets)

  return bullets
}

